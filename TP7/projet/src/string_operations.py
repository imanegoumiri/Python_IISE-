def concatenate(str1, str2):
    """
    Fonction qui concaténer des chaines.
    
    Args :
    str1 (str) : La premiere chaine.
    str2 (str) : La seconde chaine.
    
    Returns :
    str : La concaténation des deux chaines.
    """
    return str1 + str2

def uppercase(str):
    """
    Fonction qui mettre en majuscules des chaines.
    
    Args :
    str (str) : La chaine en minuscule.
    
    Returns :
    str : La chaine en majuscule.
    """
    return str.upper()
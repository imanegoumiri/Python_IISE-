def dollars_to_dirhams(dollars):
    """
    Fonction qui convertit des dollars en dirhams.
    
    Args :
    dollars (float) : Le montant en dollars.
    
    Returns :
    float : Le montant en dirhams.
    """
    taux = 10
    return dollars * taux

